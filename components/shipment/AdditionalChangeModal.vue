              />
              <div class="option-content">
                <div class="option-title">
                  <i class="fas fa-pen"></i>
                  새로 서명 받기
                </div>
                <p class="option-description">
                  문자 발송 → 서명 → 사진 촬영 절차가 필요합니다.<br />
                  기존 서명/사진 정보가 초기화됩니다.
                </p>
              </div>
            </label>
          </div>
          <p class="signature-note">
