<script setup lang="ts">
import { ref, computed, watch } from 'vue'
import { formatNumber } from '~/utils/format'
import { shipmentService } from '~/services/shipment.service'
import type { AdditionalChangeItem, AdditionalChangeResponse } from '~/types/shipment-change'
import type { Item, ItemSku } from '~/services/item.service'
import ItemSkuSelector from '~/components/admin/ItemSkuSelector.vue'

// Props
interface ShipmentItem {
  skuId: string
  itemName: string
  specification?: string
  shipmentQuantity: number
  unitPrice?: number
  unit?: string
}

interface Props {
  isOpen: boolean
  shipmentId: number
  shipmentStatus: string
  items: ShipmentItem[]
}

const props = withDefaults(defineProps<Props>(), {
  items: () => [],
  shipmentStatus: ''
})

// Emits
const emit = defineEmits<{
  (e: 'close'): void
  (e: 'complete', response: AdditionalChangeResponse): void
}>()

// State
const changeReason = ref('')
const isSubmitting = ref(false)
const reuseSignature = ref(true)
const showSkuSelector = ref(false)

interface EditableItem {
  skuId: string
  itemName: string
  specification?: string
  currentQuantity: number
  newQuantity: number
  diff: number
  isNew: boolean
  unitPrice?: number
  unit?: string
}

const editableItems = ref<EditableItem[]>([])

// Computed
const isPendingSignature = computed(() => {
  return props.shipmentStatus === 'PENDING_SIGNATURE'
})

// 원래 총 수량 (기존 품목만)
const originalTotalQuantity = computed(() => {
  return editableItems.value
    .filter(item => !item.isNew)
    .reduce((sum, item) => sum + item.currentQuantity, 0)
})

// 변경 후 총 수량 (모든 품목)
const newTotalQuantity = computed(() => {
  return editableItems.value.reduce((sum, item) => sum + (item.newQuantity || 0), 0)
})

// 총 수량 차이
const totalDiff = computed(() => {
  return newTotalQuantity.value - originalTotalQuantity.value
})

// 신규 품목 존재 여부
const hasNewItems = computed(() => {
  return editableItems.value.some(item => item.isNew)
})

// 총 수량 일치 여부 (신규 품목이 있으면 검증 건너뜀)
const isQuantityValid = computed(() => {
  if (hasNewItems.value) return true  // 신규 품목 추가 시 검증 안 함
  return Math.abs(totalDiff.value) < 0.01
})

const totalDiffClass = computed(() => {
  if (Math.abs(totalDiff.value) < 0.01) return 'text-success'
  return 'text-danger'
})

const totalDiffText = computed(() => {
  if (totalDiff.value > 0) return `+${formatNumber(totalDiff.value)}`
  if (totalDiff.value < 0) return formatNumber(totalDiff.value)
  return '0'
})

const hasChanges = computed(() => {
  // 기존 품목 수량 변경 또는 신규 품목 추가
  return editableItems.value.some(item =>
    item.isNew || item.diff !== 0
  )
})

const changedItemCount = computed(() => {
  return editableItems.value.filter(item => item.isNew || item.diff !== 0).length
})

const newItemCount = computed(() => {
  return editableItems.value.filter(item => item.isNew).length
})

const modifiedItemCount = computed(() => {
  return editableItems.value.filter(item => !item.isNew && item.diff !== 0).length
})

const changeSummaryText = computed(() => {
  const parts: string[] = []
  if (newItemCount.value > 0) {
    parts.push(`${newItemCount.value}개 품목 추가`)
  }
  if (modifiedItemCount.value > 0) {
    parts.push(`${modifiedItemCount.value}개 품목 수량 변경`)
  }
  return parts.join(', ') + '됩니다.'
})

// 자동 생성된 변경 이력
const autoGeneratedLog = computed(() => {
  const logs: string[] = []

  // 신규 추가 품목
  editableItems.value
    .filter(item => item.isNew && item.newQuantity > 0)
    .forEach(item => {
      logs.push(`[추가] ${item.itemName} ${formatNumber(item.newQuantity)}${item.unit || 'm²'}`)
    })

  // 수량 감소 품목
  editableItems.value
    .filter(item => !item.isNew && item.diff < 0)
    .forEach(item => {
      logs.push(`[감소] ${item.itemName} ${formatNumber(item.currentQuantity)} → ${formatNumber(item.newQuantity)}${item.unit || 'm²'}`)
    })

  // 수량 증가 품목
  editableItems.value
    .filter(item => !item.isNew && item.diff > 0)
    .forEach(item => {
      logs.push(`[증가] ${item.itemName} ${formatNumber(item.currentQuantity)} → ${formatNumber(item.newQuantity)}${item.unit || 'm²'}`)
    })

  return logs.join('\n')
})

const canSubmit = computed(() => {
  return changeReason.value.trim() !== '' && hasChanges.value && isQuantityValid.value
})

// Methods
const closeModal = () => {
  emit('close')
}

const initializeItems = () => {
  editableItems.value = props.items.map(item => ({
    skuId: item.skuId,
    itemName: item.itemName,
    specification: item.specification,
    currentQuantity: item.shipmentQuantity,
    newQuantity: item.shipmentQuantity,
    diff: 0,
    isNew: false,
    unitPrice: item.unitPrice,
    unit: item.unit
  }))
}

const calculateDiff = (item: EditableItem) => {
  if (item.isNew) {
    item.diff = item.newQuantity
  } else {
    item.diff = item.newQuantity - item.currentQuantity
  }
}

const getDiffClass = (item: EditableItem): string => {
  if (item.isNew) return 'text-primary'
  if (item.diff > 0) return 'text-success'
  if (item.diff < 0) return 'text-danger'
  return 'text-muted'
}

const getDiffText = (item: EditableItem): string => {
  if (item.isNew) return `+${formatNumber(item.newQuantity)}`
  if (item.diff > 0) return `+${formatNumber(item.diff)}`
  if (item.diff < 0) return formatNumber(item.diff)
  return '0'
}

// 규격에서 첫 번째 콤마 이후 부분만 표시 (품목명 제외)
const formatSpecification = (spec: string | undefined): string => {
  if (!spec) return '-'
  const commaIndex = spec.indexOf(',')
  if (commaIndex === -1) return spec
  return spec.substring(commaIndex + 1).trim()
}

// SKU 선택 팝업 열기
const openSkuSelector = () => {
  showSkuSelector.value = true
}

// SKU 선택 완료
const handleSkuSelected = (item: Item, sku: ItemSku) => {
  // 이미 추가된 SKU인지 확인
  const existingIndex = editableItems.value.findIndex(i => i.skuId === sku.skuId)
  if (existingIndex !== -1) {
    alert('이미 추가된 품목입니다.')
    return
  }

  // 규격 문자열 생성
  const specParts: string[] = []
  if (item.itemNm) specParts.push(item.itemNm)
  if (sku.skuNm) specParts.push(sku.skuNm)
  if (sku.width) specParts.push(`${sku.width}mm`)
  if (sku.height) specParts.push(`${sku.height}mm`)
  if (sku.thickness) specParts.push(`${sku.thickness}T`)

  // 신규 품목 추가
  editableItems.value.push({
    skuId: sku.skuId,
    itemName: item.itemNm,
    specification: specParts.join(', '),
    currentQuantity: 0,
    newQuantity: 0,
    diff: 0,
    isNew: true,
    unitPrice: sku.unitPrice,
    unit: 'm²'
  })

  showSkuSelector.value = false
}

// 신규 품목 삭제
const removeNewItem = (skuId: string) => {
  const index = editableItems.value.findIndex(item => item.skuId === skuId && item.isNew)
  if (index !== -1) {
    editableItems.value.splice(index, 1)
  }
}

const handleSubmit = async () => {
  if (!canSubmit.value || isSubmitting.value) return

  isSubmitting.value = true

  try {
    // 변경된 품목만 필터링
    const changedItems: AdditionalChangeItem[] = editableItems.value
      .filter(item => item.isNew || item.diff !== 0)
      .map(item => ({
        skuId: item.skuId,
        currentQuantity: item.currentQuantity,
        newQuantity: item.newQuantity,
        isNew: item.isNew,
        unitPrice: item.unitPrice,
        itemName: item.itemName,
        specification: item.specification,
        unit: item.unit
      }))

    // 변경 사유에 자동 생성된 로그 추가
    const fullChangeReason = autoGeneratedLog.value
      ? `${changeReason.value.trim()}\n\n[변경 내역]\n${autoGeneratedLog.value}`
      : changeReason.value.trim()

    // 서명대기 상태일 때만 reuseSignature 전달
    const requestData = {
      shipmentId: props.shipmentId,
      items: changedItems,
      changeReason: fullChangeReason,
      ...(isPendingSignature.value && { reuseSignature: reuseSignature.value })
    }

    const response = await shipmentService.executeAdditionalChange(requestData)

    if (response.success) {
      emit('complete', response)
    } else {
      alert(response.errorMessage || '추가변경에 실패했습니다.')
    }
  } catch (error) {
    console.error('추가변경 실패:', error)
    alert('추가변경 중 오류가 발생했습니다.')
  } finally {
    isSubmitting.value = false
  }
}

// Watch
watch(() => props.isOpen, (isOpen) => {
  if (isOpen) {
    changeReason.value = ''
    reuseSignature.value = true
    initializeItems()
  }
})
</script>

